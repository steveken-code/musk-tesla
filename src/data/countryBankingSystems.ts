// Helper function to get banking system for a country (with fallback)
export const getBankingSystem = (countryCode: string): CountryBankingSystem | null => {
  return countryBankingSystems[countryCode] || null;
};

// Get withdrawal methods based on country
export const getCountryWithdrawalMethods = (countryCode: string) => {
  const system = countryBankingSystems[countryCode];
  if (!system) {
    // Fallback for countries without specific data
    return [
      { code: 'card', name: 'Card', description: 'Bank Card' },
      { code: 'crypto', name: 'Crypto', description: 'USDT TRC20' },
    ];
  }

  const methods = [];
  
  if (system.supportsBankTransfer) {
    methods.push({
      code: 'bank',
      name: 'Bank Transfer',
      description: system.paymentSystem === 'iban' 
        ? 'IBAN Transfer' 
        : system.paymentSystem === 'routing'
          ? `${system.routingName} Transfer`
          : 'Direct Bank Transfer',
    });
  }
  
  if (system.supportsCard) {
    methods.push({
      code: 'card',
      name: 'Card',
      description: 'Bank Card',
    });
  }
  
  if (system.supportsMobilePayment && system.mobilePaymentName) {
    methods.push({
      code: 'mobile',
      name: system.mobilePaymentName,
      description: 'Mobile Payment',
    });
  }
  
  // Always add crypto
  methods.push({
    code: 'crypto',
    name: 'Crypto',
    description: 'USDT TRC20',
  });

  return methods;
};
